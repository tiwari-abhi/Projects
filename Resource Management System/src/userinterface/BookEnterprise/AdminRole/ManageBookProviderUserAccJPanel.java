    public void getBookProviderOrgs(){
                        
        for (Organization o : organizationDir.getOrganizationList()){
            
            if (o instanceof BookProviderOrg){
                BookProviderOrg org = (BookProviderOrg) o;
                bookProviderOrgList.add(org);
            }   
                
        }
    }

    public void popOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (BookProviderOrg organization : bookProviderOrgList) {
            organizationJComboBox.addItem(organization);
        }
    }
    
    public void populateEmployeeComboBox(Organization organization){
        manufacturerJComboBox.removeAllItems();
        
        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()){
            manufacturerJComboBox.addItem(employee);
        }
    }
    
    private void populateRoleComboBox(BookProviderOrg organization){
        roleJComboBox.removeAllItems();
        for (Role role : organization.getSupportedRole()){
            roleJComboBox.addItem(role);
        }
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

      for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
          if (organization.getName().equals(Organization.BookType.Provider.getValue())){
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
                }
            }
        }
    }

