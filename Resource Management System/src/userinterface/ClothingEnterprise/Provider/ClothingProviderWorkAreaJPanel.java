    private void requestTaxJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTaxJButtonActionPerformed

        int selectedRow = resourceDirectoryJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row!");
            return;
        }
        
        Resource resource = (Resource) resourceDirectoryJTable.getValueAt(selectedRow,0);

        ClothingProviderTaxRequestJPanel clothingProviderTaxRequestJPanel = new ClothingProviderTaxRequestJPanel(userProcessContainer, userAccount, 
            enterprise, business, resource,provider);
        userProcessContainer.add("ClothingProviderTaxRequestJPanel", clothingProviderTaxRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_requestTaxJButtonActionPerformed

