    public void getClothingProviderOrgs(){
        
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (o instanceof ClothingProviderOrg){
                ClothingProviderOrg org = (ClothingProviderOrg) o;
                clothingProviderOrgList.add(org);
            }
        }
        
    }
    
    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = request.getStatus();
            
            model.addRow(row);
        }
    }
    
    public Provider getLoggedInProvider(){
        
      Provider mfg=null;
      
        for (ClothingProviderOrg mo : clothingProviderOrgList){
            
            for (Provider m : mo.getClothingProviderDirectory().getProviderDirectory()){
                
                if (m.getName().equalsIgnoreCase(userAccount.getEmployee().getName())){ //Employee name must be same as mfg name.
                    mfg = m;
                    break;
                }
            }
        }
        return mfg;
    }
    
    public void populateDonatedResourceTable(){
        DefaultTableModel model = (DefaultTableModel) resourceDirectoryJTable.getModel();
        model.setRowCount(0);
        
        for (Resource r : provider.getDonatedResources().getResourceList()){
            Object row[] = new Object[4];
            row[0] = r;
            row[1] = r.getTotalDonatedToSystem();
            row[2] = r.getUnitPrice();
            row[3] = r.getUnitPrice() * r.getTotalDonatedToSystem();
            model.addRow(row);
        }
        
    }

    
