    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }

        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        if (request.getStatus().equalsIgnoreCase("Pending")){
            JOptionPane.showMessageDialog(null,"Request has already been assigned","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateRequestTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed
         int selectedRequest = workRequestJTable.getSelectedRow();

        if (selectedRequest < 0){
            JOptionPane.showMessageDialog(null,"Please select row to proceed");
            return;
        }
                
        NGOResourceWorkRequest request = (NGOResourceWorkRequest)workRequestJTable.getValueAt(selectedRequest, 0);

        request.setStatus("Processing");

        ClothingShippingJPanel drvjp = new ClothingShippingJPanel(userProcessContainer,userAccount,enterprise,organization,
        business,request);
        userProcessContainer.add("BookShippingJPanel", drvjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    
    }//GEN-LAST:event_requestTestJButtonActionPerformed

