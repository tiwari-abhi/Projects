        public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            
            if (request instanceof NGOResourceWorkRequest){
                
                NGOResourceWorkRequest req = (NGOResourceWorkRequest) request;
                
                Object[] row = new Object[6];
                row[0] = req;
                row[1] = req.getQuantityNeeded();
                row[2] = req.getMessage();
                row[3] = req.getSender().getEmployee().getName();
                row[4] = req.getReceiver() == null ? null : req.getReceiver().getEmployee().getName();
                row[5] = req.getStatus();
                model.addRow(row);
                
            }
        }
    }
    
