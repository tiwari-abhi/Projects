    public void populateProviderResourceTable(Resource.ResourceType type){
        
        DefaultTableModel model = (DefaultTableModel)resourceDirectoryJTable.getModel();
        int count=0;
        model.setRowCount(0);
                
        for (Resource r : organization.getResourcesReceivedDir().getResourceList()){
           
            if (r.getResourceType().equalsIgnoreCase(type.getValue())){
            
                Object[] row = new Object[4];            
                row[0] = r;
                row[1] = r.getTotalDonatedToNGO();
                row[2] = r.getUnitPrice();
                row[3] = r.getTotalDonatedToNGO() * r.getUnitPrice();
                model.addRow(row);
                count += r.getTotalDonatedToNGO();
            
            }
        }
              
      lbltotalResources.setText(String.valueOf(count));
      
    }
    
    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            
            if (request instanceof NGOResourceWorkRequest){
                
                NGOResourceWorkRequest req = (NGOResourceWorkRequest) request;
                
                Object[] row = new Object[5];
                row[0] = req.getResourceName();
                row[1] = req;
                row[2] = req.getSender().getEmployee().getName();
                row[3] = req.getReceiver() == null ? null : req.getReceiver().getEmployee().getName();
                row[4] = req.getStatus();
                model.addRow(row);
                
            }
        }
    }

    
