    private void removeResourceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeResourceActionPerformed
        // TODO add your handling code here:
        int getselectedResource = resourceDirectoryJTable.getSelectedRow();
        Resource.ResourceType type = (Resource.ResourceType) resourceComboBox.getSelectedItem();
        
        if (getselectedResource<0){
            JOptionPane.showMessageDialog(null,"Please select a row !");
            return;
        }
        
        Resource r = (Resource) resourceDirectoryJTable.getValueAt(getselectedResource,0);
        
        for (Resource res : organization.getResourcesReceivedDir().getResourceList()){
            if ((res.getResourceType().equalsIgnoreCase(type.getValue())) && (res.equals(r))){
                organization.getResourcesReceivedDir().removeResource(res);
                break;
            }
        }
        
        populateProviderResourceTable(type);
    }//GEN-LAST:event_removeResourceActionPerformed

