    private void notifyGovernmentJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_notifyGovernmentJButtonActionPerformed
        // TODO add your handling code here:
        GovernmentEnterprise org = null; //Enterprise since Govt has only admin organization
        NotifyGovernmentWorkRequest request=null;
        
        if (donatedResource.getResourceType().equals(Resource.ResourceType.Book.getValue())){
            BookResource br = (BookResource) donatedResource;
            request = new NotifyGovernmentWorkRequest(br);
            
            request.setTaxBenefitValue(donatedResourceValue * br.getBookTaxbenefitPercent());
            request.setMessage("Approval for " + br.getBookName() + "sent successfully");
        }
        else if (donatedResource.getResourceType().equals(Resource.ResourceType.Clothing.getValue())){
            ClothingResource cr = (ClothingResource) donatedResource;
            request = new NotifyGovernmentWorkRequest(cr);
            
            request.setTaxBenefitValue(donatedResourceValue * cr.getClothesTaxBenefitPercent());
            request.setMessage("Approval for " + cr.getClothingItemName() + "sent successfully");
        }
        
        
        Network n = findNetwork();
            
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                
                if (e instanceof GovernmentEnterprise){
                    org = (GovernmentEnterprise) e ;
                    break;
                }
                
            }
        
        
        
        request.setDonatedResourceValue(donatedResourceValue);
        request.setSender(account);
        request.setStatus("Government Notification Sent");

        
        if (org != null){
            org.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
        }
        
        
        JOptionPane.showMessageDialog(null,"Resource Notification sent to local Government!");
                        
    }//GEN-LAST:event_notifyGovernmentJButtonActionPerformed

