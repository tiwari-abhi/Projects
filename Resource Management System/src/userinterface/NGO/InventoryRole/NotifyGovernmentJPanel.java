    public Network findNetwork(){
        for (Network n : business.getNetworkList()){
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                for (Organization o : e.getOrganizationDirectory().getOrganizationList()){
                    for (UserAccount ua : o.getUserAccountDirectory().getUserAccountList()){
                        if (account.equals(ua)){
                            return n;
                        }
                    }
                }
            }
        }
        return null;
    }
    
    public void settext(){
        
        if (donatedResource.getResourceType().equals(Resource.ResourceType.Book.getValue())){
            
            BookResource br = (BookResource) donatedResource;
            resourceTypeJTextField.setText(Resource.ResourceType.Book.getValue());
            resourceNameJTextField.setText(br.getBookName());
            resourceQuantityJTextField.setText(String.valueOf(br.getTotalDonatedToNGO()));
            
            donatedResourceValue = br.getTotalDonatedToNGO() * br.getUnitPrice();
            
            donResValueJTextField.setText(String.valueOf(donatedResourceValue));
            taxBenefitValueJTextField.setText(String.valueOf(donatedResourceValue * br.getBookTaxbenefitPercent()));
        }
        
        else if (donatedResource.getResourceType().equals(Resource.ResourceType.Clothing.getValue())){
            ClothingResource br = (ClothingResource) donatedResource;
            resourceTypeJTextField.setText(Resource.ResourceType.Clothing.getValue());
            resourceNameJTextField.setText(br.getClothingItemName());
            resourceQuantityJTextField.setText(String.valueOf(br.getTotalDonatedToNGO()));
            
            donatedResourceValue = br.getTotalDonatedToNGO() * br.getUnitPrice();
            
            donResValueJTextField.setText(String.valueOf(donatedResourceValue));
            taxBenefitValueJTextField.setText(String.valueOf(donatedResourceValue * br.getClothesTaxBenefitPercent()));
        }
        
    }

