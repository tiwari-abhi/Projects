    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        
        if (request.getStatus().equalsIgnoreCase("Processed")){
            JOptionPane.showMessageDialog(null,"The request has already been processed","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        request.setApprovalDecision("Approved");
        request.setStatus("Processed");
        enterprise.getProcessedRequestList().add(request);
        JOptionPane.showMessageDialog(null,"Request Approved");

        sendEmail("aed.finalproject@gmail.com", "resource", request.getProvider().getEmail(), "Approval Status Update || Resource Request Approved ", "Dear " + request.getSender().getEmployee().getName() + "," + "Congratulations, Your request for tax benefit of resource name : " + request.getDonatedResource().toString()+ " with a total donated quantity of " + request.getDonatedResource().getTotalDonatedToSystem()+ " has been approved !");
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
        
        if (request.getStatus().equalsIgnoreCase("Processed")){
            JOptionPane.showMessageDialog(null,"The request has already been processed","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        request.setApprovalDecision("Rejected");
        request.setStatus("Processed");
        enterprise.getProcessedRequestList().add(request);
        JOptionPane.showMessageDialog(null,"Request Rejected");
        
        sendEmail("aed.finalproject@gmail.com", "resource", request.getProvider().getEmail(), "Approval Status Update || Resource Request Rejected ", "Dear " + request.getSender().getEmployee().getName() + "," + "Sorry, Your request for tax benefit of resource name : " + request.getDonatedResource().toString()+ " with a total donated quantity of " + request.getDonatedResource().getTotalDonatedToSystem()+ " has been rejected !");
        
    }//GEN-LAST:event_btnRejectActionPerformed

