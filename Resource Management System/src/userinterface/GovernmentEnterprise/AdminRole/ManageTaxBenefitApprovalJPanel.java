    public void popNGOResReqTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);
        
        for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()){
            if (wr instanceof NotifyGovernmentWorkRequest){
                NotifyGovernmentWorkRequest ngwr = (NotifyGovernmentWorkRequest) wr;
                Object[] row = new Object[4];
                row[0] = ngwr;
                row[1] = ngwr.getDonatedResource().getTotalDonatedToNGO();
                row[2] = ngwr.getDonatedResourceValue();
                row[3] = ngwr.getTaxBenefitValue();
                model.addRow(row);
            }
        }
        
    }
    
    public void sendEmail(String from,String password,String to,String sub,String msg){
      Properties props = new Properties();
      props.put("mail.smtp.host", "smtp.gmail.com");
      props.put("mail.smtp.socketFactory.port", "465");
      props.put("mail.smtp.socketFactory.class",
              "javax.net.ssl.SSLSocketFactory");
      props.put("mail.smtp.auth", "true");
      props.put("mail.smtp.port", "465");
      //get Session  
      Session session = Session.getDefaultInstance(props,
              new javax.mail.Authenticator() {
          protected javax.mail.PasswordAuthentication getPasswordAuthentication() {
              return new javax.mail.PasswordAuthentication(from, password);
          }
      });
      //compose message    
      try {
          MimeMessage message = new MimeMessage(session);
          message.setFrom(new InternetAddress(from));
          message.addRecipient(Message.RecipientType.TO, new InternetAddress(to));
          message.setSubject(sub);
          message.setText(msg);
          //send message  
          javax.mail.Transport.send(message);
          System.out.println("message sent successfully");
      } catch (javax.mail.MessagingException e) {
          throw new RuntimeException(e);
      }
    }

