    private void btnSetPercentBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetPercentBookActionPerformed
        // TODO add your handling code here:
        if (!txtBookTaxBenefitPercent.getText().isEmpty()){
            double val = Double.parseDouble(txtBookTaxBenefitPercent.getText());
            if (val < 0){
                JOptionPane.showMessageDialog(null,"Percentage cannot be negative","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
            business.getBtb().setBooktaxBenefitPercentage(val/100);
            enterprise.getBookTaxPercentages().add(val/100);
            txtBookTaxBenefitPercent.setEnabled(false);
            btnModifyPercentBook.setEnabled(true);
            btnSetPercentBook.setEnabled(false);
            
            JOptionPane.showMessageDialog(null,"Tax Benefit Percentage Changed!");
        }
        else 
            JOptionPane.showMessageDialog(null,"Please do not leave field blank","Warning",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnSetPercentBookActionPerformed

    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }

        ProviderTaxBenefitWorkRequest request = (ProviderTaxBenefitWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        if (request.getStatus().equalsIgnoreCase("Pending")){
            JOptionPane.showMessageDialog(null,"Request has already been assigned");
            return;
        }
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateRequestTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

