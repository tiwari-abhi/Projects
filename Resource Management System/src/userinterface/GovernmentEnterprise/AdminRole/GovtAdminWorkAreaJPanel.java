    private void manageTaxApprovalJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manageTaxApprovalJButtonActionPerformed
        // TODO add your handling code here:
        int getselectedRow = workRequestJTable.getSelectedRow();
        
        if (getselectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }
        
        ProviderTaxBenefitWorkRequest request = (ProviderTaxBenefitWorkRequest) workRequestJTable.getValueAt(getselectedRow, 0);
        
        ManageTaxBenefitApprovalJPanel mtbajp = new ManageTaxBenefitApprovalJPanel(userProcessContainer,userAccount, 
            organization, enterprise, business,request);
        userProcessContainer.add ("ManageTaxBenefitApprovalJPanel", mtbajp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_manageTaxApprovalJButtonActionPerformed

