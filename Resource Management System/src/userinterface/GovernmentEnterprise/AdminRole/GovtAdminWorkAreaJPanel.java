    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()){
            
            if (request instanceof ProviderTaxBenefitWorkRequest){
                
                ProviderTaxBenefitWorkRequest req = (ProviderTaxBenefitWorkRequest) request;
                                    
                    Object[] row = new Object[6];
                    row[0] = req;
                    row[1] = req.getDonatedResource().getTotalDonatedToSystem();
                    row[2] = req.getDonatedResource().getUnitPrice() * req.getDonatedResource().getTotalDonatedToSystem();
                    row[3] = req.getSender().getEmployee().getName();
                    row[4] = req.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
                    row[5] = req.getStatus();

                    model.addRow(row);
            }
        }
    }   
    
