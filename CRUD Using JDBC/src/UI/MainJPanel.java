    private void enterJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterJButtonActionPerformed
        // TODO add your handling code here:
         if (!(appNameJTextField.getText().isEmpty() || appDescJTextField.getText().isEmpty() || appReleaseDateJTextField.getText().isEmpty() || costJTextField.getText().isEmpty())) {
            try {
                String name = appNameJTextField.getText();
                String desc = appDescJTextField.getText();
                String relDate = appReleaseDateJTextField.getText();
                String cost = costJTextField.getText();

                setToDB(name,desc,relDate,cost);

                JOptionPane.showMessageDialog(null,"Application added successfully");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Could not add Application");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please do not leave fields blank");
            return;
        }
    }//GEN-LAST:event_enterJButtonActionPerformed


