    private void connectoDB() {
        String driver = "com.microsoft.sqlserver.jdbc.SQLServerDriver";
        String server = "localhost\\SQLEXPRESS";
        int port = 51402;
        String user = "sa"; // Sql server username
        String password = "admin@1234";
        String database = "ApplicationDB";
 
        String jdbcUrl = "jdbc:sqlserver://"+server+":"+port+";user="+user+";password="+password+";databaseName="+database+"";
        
        connect(driver,jdbcUrl,user,password);
        
    }
    
    public void connect(String driver, String url ,String user ,String password){
        try {
            Class.forName(driver);
        } catch (ClassNotFoundException ex) {
            System.out.println("Cannot load driver");
        }
        
        try {
            conn = DriverManager.getConnection(url);
            JOptionPane.showMessageDialog(null, "Connected to DB");
        } catch (SQLException ex) {
            System.out.println("Cannot connect to database");
        }
    }
    
    public void setToDB(String name,String desc,String relDate,String cost) throws SQLException{
        PreparedStatement write = conn.prepareStatement("EXEC data_insert ?,?,?,?");
        
        write.setString(1, name);
        write.setString(2, desc);
        write.setString(3, relDate);
        write.setString(4, cost);
        
        write.execute();
        
        write.close();
    }
    
    
